package math.math;

import java.util.Random;

public class MathMethods {
    public static void main(String[] args) {
        System.out.println(Math.max(5,8));
        System.out.println(Math.min(5,8));
        System.out.println(Math.PI);
        System.out.println(Math.round(Math.PI));
        System.out.println(Math.round(15.54));
        System.out.println(Math.abs(-8));
        System.out.println(Math.negateExact(-5));
        System.out.println(Math.addExact(5,6));
        System.out.println(Math.subtractExact(10,5));
        System.out.println(Math.multiplyExact(5, 5));
        System.out.println(Math.pow(5.5, 5.5));
        System.out.println(Math.incrementExact(10));
        System.out.println(Math.decrementExact(10));
        System.out.println(Math.random());

    }

}
